    v_cursor := MOSTRAR_DATOS_DE_CONDUCTORES();
    LOOP
        FETCH v_cursor INTO v_cedula, v_nombre, v_apellido1, v_apellido2;
        EXIT WHEN v_cursor%NOTFOUND;
